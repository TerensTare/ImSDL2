    // GL ES 2.0 + GLSL 100
    ImGui_ImplOpenGL3_Init("#version 100");
#elif defined(__APPLE__)
    // GL 3.2 Core + GLSL 150
    ImGui_ImplOpenGL3_Init("#version 150");
#else
